package models;

import javax.persistence.*;

/**
 *
 * @author joycee
 */
@Entity
@Table(name = "tags")
public class Tags {
    @Id @GeneratedValue
    private int id;
    private String title;

    public Tags(String title) {
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
